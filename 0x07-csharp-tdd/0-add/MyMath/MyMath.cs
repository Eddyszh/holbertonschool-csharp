using System;

namespace MyMath
{
    /// <summary>
    /// Class for math operations
    /// </summary>
    public class Operations
    {
        /// <summary>
        /// Adds two integers
        /// </summary>
        /// <return>
        /// sum of a and b
        /// </return>
        public static int Add(int a, int b)
        {
            return a + b;
        }
    }
}
